    private void populateTables(){
        //officer table
        table1 = (DefaultTableModel) voterTable.getModel();
        table1.setRowCount(0);
     //   ArrayList<Users> beans =  new ArrayList<Users>(users.values());
        
        
        for( Map.Entry<Integer, Users> entry : users.entrySet()){
            Users u = entry.getValue();
            if(u.getType().equals(GlobalConstants.VOTER)){
                table1.addRow(new Object[]{
                u.getID(), u.getFirst_name() + " " + u.getLast_name()
                
                });
            }
            
        }
        
        //voter table
        table2 = (DefaultTableModel) officerTable.getModel();
        table2.setRowCount(0);
        
        for( Map.Entry<Integer, Users> entry : users.entrySet()){
            Users u = entry.getValue();
            if(u.getType().equals(GlobalConstants.OFFICER)){
                table2.addRow(new Object[]{
                u.getID(), u.getFirst_name() + " " + u.getLast_name()
                
                });
            }
            
        }
        
        
    }

